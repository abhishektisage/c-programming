//WAP to accept length & width of rectangle and calculate area of rectangle
#include<stdio.h>
void main()
{
	int l,w,area;
	printf("enter the lenth of rectangle\n");
	scanf("%d",&l);
	printf("enter the width of rectangle\n");
	scanf("%d",&w);
	
	area=l*w;
	printf("area of rectangle is %d",area);
	
	
}
